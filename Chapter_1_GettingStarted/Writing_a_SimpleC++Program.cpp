// Every C++ program contains one or more functions, one of which must be named main
// The operating system runs a C++ program by calling main function

// A function definition has four elements
// 01. return type -> int
// 02. function name -> main
// 03. parameter list enclosed in parentheses -> ()
// 04. function body -> {}

// C++ start
int main()
{
    return 0;
    //  return a value to the operating system that terminates a function
    //  A return 0 means that the program will execute successfully
    //  A return 1 means that there is some error while executing the program
}

